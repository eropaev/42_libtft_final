#include "libft.h"

long	ft_sizenb_l(long n)
{
	long	i;
	long	nb;

	i = 0;
	nb = n;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		i++;
		nb = -nb;
	}
	while (nb != 0)
	{
		nb = nb / 10;
		i++;
	}
	return (i++);
}
